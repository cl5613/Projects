package Regex;

public class regex {

    public static void main(String[] args) {

        /*

        [abc]
        [^abc]

        .    any string but one
        \d   one digit
        \D   non-digit
        \s   empty string
        \S   non-empty string
        \w   [a-zA-z_0-9]
        \W   [^\w]
        \

        */

        String regex = " ";
        System.out.println(" ".matches(regex));

    }
}
